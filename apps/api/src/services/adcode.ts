/** 需要从名称尾部剥离的行政/民族后缀，长后缀优先以避免误删。 */
const SUFFIXES = [
  '维吾尔自治区',
  '壮族自治区',
  '回族自治区',
  '特别行政区',
  '自治区',
  '自治州',
  '自治县',
  '维吾尔',
  '壮族',
  '回族',
  '苗族',
  '彝族',
  '藏族',
  '蒙古',
  '省',
  '市',
  '区',
  '县',
];

/** 去空白并剥离常见行政/民族后缀，得到便于匹配的核心名称。 */
function normalize(raw?: string | null): string {
  if (!raw) return '';
  let s = raw.trim();
  if (!s) return '';
  // 反复剥离尾部后缀，处理“内蒙古自治区呼和浩特市”这类组合。
  let changed = true;
  while (changed) {
    changed = false;
    for (const suffix of SUFFIXES) {
      if (s.length > suffix.length && s.endsWith(suffix)) {
        s = s.slice(0, -suffix.length);
        changed = true;
        break;
      }
    }
  }
  return s;
}

/** 在字典中宽松匹配：先精确，再双向子串。 */
function lookup(name: string, dict: Record<string, string>): string | null {
  if (!name) return null;
  if (dict[name]) return dict[name];
  for (const key of Object.keys(dict)) {
    if (name.includes(key) || key.includes(name)) {
      return dict[key];
    }
  }
  return null;
}

export interface ResolveAdcodeInput {
  province?: string | null;
  city?: string | null;
}

export interface ResolveAdcodeResult {
  provinceId: string | null;
  cityId: string | null;
}

/**
 * 根据省/市名称解析 adcode。
 * - 命中城市时，若省份未命中，则用城市 adcode 前两位 + '0000' 反推省份。
 */
export function resolveAdcode(input: ResolveAdcodeInput): ResolveAdcodeResult {
  const provinceName = normalize(input.province);
  const cityName = normalize(input.city);

  let provinceId = lookup(provinceName, PROVINCE_ADCODE);
  const cityId = lookup(cityName, CITY_ADCODE);

  if (!provinceId && cityId) {
    provinceId = `${cityId.slice(0, 2)}0000`;
  }

  return { provinceId, cityId };
}
